import math

# Calculate the square root of 25
print("Square root of 25:", math.sqrt(25))

# Calculate the factorial of 5 (5! = 5 * 4 * 3 * 2 * 1)
print("Factorial of 5:", math.factorial(5))

# Round 5.26 to the nearest integer
print("Rounded 5.26:", round(5.26))

# Round 5.26 to one decimal place
print("Rounded 5.26 to 1 decimal:", round(5.26, 1))

# Round 5.24 to one decimal place
print("Rounded 5.24 to 1 decimal:", round(5.24, 1))
